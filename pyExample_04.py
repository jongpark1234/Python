# Variable

# My name is JongPark and I am 17 years old.

name = "JongPark" # ( str )
age = 17 # ( int )

print(name) # JongPark
print(age) # 17

# Variables can be used together with basic characters.

print("Name : " + name) # Name : JongPark
print("Age : " + str(age)) # Age : 17
print("Name : " + name + ", Age : " + str(age)) # Name : JongPark, Age : 17