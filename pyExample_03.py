# Boolean Type Data

# In the case of Boolean type data, the first letter must be capitalized.

print(True) # True
print(False) # False

# 1 is returned to 'True' and 0 is returned to 'False'.

print(bool(0)) # False
print(bool(1)) # True

# All nonzero numbers return 'True'.

print(bool(1.1)) # True
print(bool(-2)) # True

# Whether the value is 'True' or 'False' can return the bool can be returned.

print(3 > 5) # False
print(10 > 3) # True

# If you put "not", the bool will be reversed.

print(not False) # True
print(not 3 < 5) # False 
