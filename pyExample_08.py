# Number Processing Function

a = 10
b = 5

# 'abs' returns the absolute value.
print(abs(-a)) # 10 ( |-a| )

# 'pow' returns the left value squared by the right value.
print(pow(10, 5)) # 100000 ( 10^5 )

# 'max' returns the larger of the two values.
print(max(a, b)) # 10 ( a > b )

# 'min' returns a smaller of the two values.
print(min(a, b)) # 5 ( b < a )

# 'round' returns the value on the left rounded to decimal places by the value on the right.
# If you receive only one value, round the received value to the place of work.
print(round(3.14)) # 3
print(round(3.14, 1)) # 3.1
print(round(3.141592, b - 1)) # 3.1416
print(round(426.34, -1)) # 430.0