# Escape Character

print("I am\nJongPark")
# I am
# JongPark

print("I am \"JongPark\".") # I am "JongPark". 
# \" -> "

print('I am \'JongPark\'.') # I am 'JongPark".
# \' -> '

print("C:\\Users\\DGSW\\Documents\\Github\\Python") # C:\Users\DGSW\Documents\GitHub\Python
# \\ -> \.

print("ABCDPark\rJong") # JongPark
# Move the corsor to start pos.

print("Jongg\bPark") # JongPark
# \b = backspace

print("Jong\tPark") # Jong  Park
# \t = tab